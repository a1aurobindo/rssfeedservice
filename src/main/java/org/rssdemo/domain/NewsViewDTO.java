package org.rssdemo.domain;

import java.util.Date;

public class NewsViewDTO {

    String newsId;

    String newsTitle;

    String newsDescription;

    Date newsPublishDateTime;

    String newsLink;

    Integer clickCount;

    Integer categoryId;

    Integer agencyId;

    public String getNewsId() {
        return newsId;
    }

    public void setNewsId(String newsId) {
        this.newsId = newsId;
    }

    public String getNewsTitle() {
        return newsTitle;
    }

    public void setNewsTitle(String newsTitle) {
        this.newsTitle = newsTitle;
    }

    public String getNewsDescription() {
        return newsDescription;
    }

    public void setNewsDescription(String newsDescription) {
        this.newsDescription = newsDescription;
    }

    public Date getNewsPublishDateTime() {
        return newsPublishDateTime;
    }

    public void setNewsPublishDateTime(Date newsPublishDateTime) {
        this.newsPublishDateTime = newsPublishDateTime;
    }

    public String getNewsLink() {
        return newsLink;
    }

    public void setNewsLink(String newsLink) {
        this.newsLink = newsLink;
    }

    public Integer getClickCount() {
        return clickCount;
    }

    public void setClickCount(Integer clickCount) {
        this.clickCount = clickCount;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public Integer getAgencyId() {
        return agencyId;
    }

    public void setAgencyId(Integer agencyId) {
        this.agencyId = agencyId;
    }
}
